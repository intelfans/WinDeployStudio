// Deployment 2.0 strings are kept separate so every supported locale has the
// same explicit key set. Do not add locale fallbacks here.

const _deploymentLocaleCodes = <String>[
  'zh',
  'zh_TW',
  'en',
  'fr',
  'de',
  'es',
  'pt',
  'ru',
  'ar',
  'ko',
  'ja',
];

typedef _Translations = List<String>;

Map<String, String> deploymentStringsForCode(String code) =>
    _deploymentStrings[code] ?? const <String, String>{};

final Map<String, Map<String, String>> _deploymentStrings =
    _buildDeploymentStrings();

Map<String, Map<String, String>> _buildDeploymentStrings() {
  final result = <String, Map<String, String>>{
    for (final code in _deploymentLocaleCodes) code: <String, String>{},
  };
  for (final entry in _deploymentRows.entries) {
    assert(
      entry.value.length == _deploymentLocaleCodes.length,
      '${entry.key} must define all deployment locales',
    );
    for (var index = 0; index < _deploymentLocaleCodes.length; index++) {
      result[_deploymentLocaleCodes[index]]![entry.key] = entry.value[index];
    }
  }
  return result;
}

